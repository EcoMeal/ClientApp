package ecomeal.client.entity;

public class BasketCategory {
	
	private int id;
	
	private String name;
	
	public BasketCategory() {	
	}

	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
}
